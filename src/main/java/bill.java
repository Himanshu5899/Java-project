    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String Sn=TokenNm.getText();
        String RegId=RegNm.getText();
        String Hname=HName.getText();
        String add =Address.getText();
        String bloodGroup =BGroup.getText();
        String unit =BUnit.getText();
       String Dis = Distance.getText();
       String amt = Amount.getText();
       String date = Date.getText();
    
    
    
    try {
        Connection con = ConnectionProvider.getCon();
        Statement st = con.createStatement();
         PreparedStatement ptr=null;
        // Check if the requested units are available in stock {
               String query ="Insert into Bill(Token,RegNo,HospitalName,Address,BloodGroup,Unit,Distance,Amount,Date)values(?,?,?,?,?,?,?,?,?)";
                ptr =con.prepareStatement(query);
                ptr.setString(1,Sn);
                ptr.setString(2,RegId);
                ptr.setString(3,Hname);
                 ptr.setString(4,add);
                 ptr.setString(5,bloodGroup);
                 ptr.setString(6,unit);  
                 ptr.setString(7,Dis);
                 ptr.setString(8,amt);
                 ptr.setString(9,date);
                 ptr.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Bill Successfully Save");
                setVisible(false);
                new bill().setVisible(true);
         
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }

    }//GEN-LAST:event_jButton5ActionPerformed

