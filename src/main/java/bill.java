    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String token=TokenNm.getText();
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            var rs=st.executeQuery("select *FROM orderdetails where SNo='"+token+"'");
            if(rs.next()){
            RegNm.setText(rs.getString(2));
            HName.setText(rs.getString(3));
            Address.setText(rs.getString(4));
            BGroup.setText(rs.getString(5));
            BUnit.setText(rs.getString(6));
            Date.setText(rs.getString(7));
            rs=st.executeQuery("select *FROM delivery where Token='"+token+"'");
            if(rs.next()){
            Distance.setText(rs.getString(4));
            Amount.setText(rs.getString(6));
            }
            }
            else
                JOptionPane.showMessageDialog(null,"Hospital Id does not exist");
           }
        catch(Exception e){}
    }//GEN-LAST:event_jButton1ActionPerformed

